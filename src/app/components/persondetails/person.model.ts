export interface Person {
    id: number;        // Unique identifier for each person
    name: string;      // Full name of the person
    address: string;   // Address of the person
    city: string;      // City where the person lives
    state: string;     // State where the person lives
    zipCode: string;   // Zip code of the person's address
    phoneNumber: string; // Phone number of the person
  }